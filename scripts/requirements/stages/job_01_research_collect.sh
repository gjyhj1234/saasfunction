## 目标用户画像

### 1. 诊所管理者 / 诊所老板
- 关注: 运营效率、成本控制、多分店管理、耗材采购成本
- 痛点: 多系统切换、数据孤岛、耗材采购渠道分散、缺乏统一库房管理
- 东南亚特点: 很多诊所老板同时是主治医生，需要简洁高效的管理工具

### 2. 口腔医生
- 关注: 病历录入效率、影像查看、治疗方案
- 痛点: 操作繁琐、移动端体验差、多语言病历支持不足
- 东南亚特点: 可能需要用英语+本地语言双语记录病历

### 3. 前台/客服
- 关注: 预约调度、患者沟通、收费开票
- 痛点: 预约冲突、多渠道沟通（WhatsApp/LINE/电话）、多币种结算
- 东南亚特点: 需要集成本地即时通讯工具，支持牙科旅游患者的跨国预约

### 4. 患者（含牙科旅游患者）
- 关注: 在线预约、费用透明、复诊提醒、口腔护理产品购买
- 痛点: 就诊流程不透明、跨国就医语言障碍、术后护理产品难购买
- 东南亚特点: 牙科旅游患者需要英语界面、在线预约、费用预估

### 5. 库房管理员 / 采购人员
- 关注: 耗材入库出库、库存盘点、采购审批、供应商管理
- 痛点: 手工记录易出错、缺乏预警、多供应商价格对比困难
- 东南亚特点: 进口耗材管理、海关清关跟踪、多币种采购

### 6. 商城运营（B2B/B2C）
- 关注: 口腔护理产品上架、订单管理、物流跟踪
- 痛点: 诊所零售与线上商城库存不同步
- 东南亚特点: 需要对接 Shopee/Lazada 等东南亚电商平台

## 核心需求优先级

1. **P0 — 必须**: 预约管理、患者档案、收费结算（多币种）、用户权限
2. **P0 — 必须**: 仓储管理（库房进销存、耗材追踪）
3. **P1 — 重要**: 电子病历、报表分析、多语言支持
4. **P1 — 重要**: 商城系统（B2C 口腔护理产品 + B2B 耗材采购）
5. **P2 — 增强**: 患者沟通（WhatsApp/LINE 集成）、影像管理、AI 辅助、牙科旅游支持
"

