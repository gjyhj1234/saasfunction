## 2. 东南亚市场概况

### 2.1 市场特征
- 东南亚牙科市场快速增长，尤其在泰国、越南、菲律宾、印尼、马来西亚
- 牙科旅游 (Dental Tourism) 是泰国、越南的重要收入来源
- 数字化程度参差不齐：新加坡/马来西亚较高，缅甸/老挝/柬埔寨较低
- 多语言需求：英语、泰语、越南语、印尼语、马来语等
- 多币种支付：THB、VND、IDR、MYR、SGD、PHP、USD
- 法规差异：各国医疗数据合规要求不同

### 2.2 与中国大陆市场的核心差异
| 维度 | 中国大陆 | 东南亚 |
|------|----------|--------|
| 支付方式 | 微信/支付宝/医保 | GrabPay/GCash/OVO/银行转账/现金/信用卡 |
| 语言 | 中文单一 | 多语言（英语为通用+本地语言） |
| 医保体系 | 国家医保为主 | 私人保险为主，部分国家有公共医保 |
| 诊所规模 | 连锁化程度高 | 独立诊所为主，连锁化正在发展 |
| 患者沟通 | 微信/短信 | WhatsApp/LINE/Viber/Telegram |
| 法规合规 | 中国数据安全法 | PDPA(泰国)/PDP(印尼)/PDPA(新加坡)等 |
| 耗材供应链 | 国内供应商为主 | 进口依赖度高，供应链复杂 |
| 商城需求 | 较少 | 诊所零售(口腔护理产品)+B2B耗材采购需求强 |

