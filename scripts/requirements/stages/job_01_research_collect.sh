> 生成时间: $(now_ts)
> 执行范围: ${SCOPE}

## 目标用户画像

### 1. 诊所管理者
- 关注: 运营效率、成本控制、数据分析
- 痛点: 多系统切换、数据孤岛

### 2. 口腔医生
- 关注: 病历录入效率、影像查看、治疗方案
- 痛点: 操作繁琐、移动端体验差

### 3. 前台/客服
