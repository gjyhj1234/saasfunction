#!/usr/bin/env bash
# job_03_product_scope.sh — 定义产品范围（东南亚市场 + 仓储/商城）
set -euo pipefail
source "$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)/common.sh"
SCOPE="${1:?scope required}"

DOC_DIR="${DOCS_DIR}/02-architecture"
ensure_dir "$DOC_DIR"

write_doc "$DOC_DIR/product-scope.md" "# 产品范围定义

> 生成时间: $(now_ts)
> 执行范围: ${SCOPE}
> 目标市场: 东南亚 (Southeast Asia)

## 产品愿景

打造一款面向东南亚市场的现代化、AI 增强的口腔诊所 SaaS 管理系统，为东南亚口腔医疗机构提供从预约到结算的全流程数字化解决方案，并通过仓储管理和商城系统构建口腔行业供应链生态。

## 目标市场

- **主要市场**: 泰国、越南、菲律宾、印尼、马来西亚
- **次要市场**: 新加坡、缅甸、柬埔寨
- **特殊场景**: 牙科旅游 (Dental Tourism)

## MVP 范围定义

### 核心模块 (MVP 必须)

| 模块 | 说明 | 用户角色 |
|------|------|----------|
| 预约管理 | 预约创建、日历视图、冲突检测 | 前台、医生、患者 |
| 患者档案 | 患者信息、就诊历史 | 前台、医生 |
| 收费结算 | 费用计算、账单、多币种收款 | 前台、管理者 |
| 用户与权限 | 登录、RBAC、多角色 | 全部 |
| 仓储管理 (WMS) | 仓库/库位、进销存、采购管理 | 库管、采购、管理者 |

### 二期扩展模块

| 模块 | 说明 |
|------|------|
| 电子病历 | 结构化病历、牙位图 |
| 商城系统 | B2C 口腔护理产品商城 + B2B 耗材采购平台 |
| 临床库存 | 诊室级耗材领用管理 |
| 报表分析 | 经营数据、可视化 |
| 多语言支持 | 英/泰/越/印尼/马来语 |

### 远期规划模块

| 模块 | 说明 |
|------|------|
| 影像管理 | DICOM 集成 |
| AI 辅助 | 智能诊断建议 |
| 连锁管理 | 多门店数据汇总 |
| 牙科旅游 | 跨国预约/多语言沟通 |
| 患者沟通 | WhatsApp/LINE/Viber 集成 |
| 电商平台对接 | Shopee/Lazada 集成 |

## 技术边界

- **前端**: React / Next.js
- **后端**: Node.js / NestJS
- **数据库**: PostgreSQL
- **部署**: Docker + Kubernetes
- **认证**: JWT + OAuth2
- **多租户**: Schema-per-tenant
- **国际化**: i18next (前端) + nestjs-i18n (后端)
- **多币种**: 基于 ISO 4217 标准

## 排除项

以下内容明确不在本项目范围内：
- 中国大陆市场本地化（微信支付、支付宝、中国医保）
- 医疗影像 AI 分析
- 药品管理（非口腔专用）
- 医院 HIS 系统集成
- 硬件设备驱动开发
- 自建物流体系
"

echo "$DOC_DIR/product-scope.md"
