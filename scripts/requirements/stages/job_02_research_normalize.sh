#!/usr/bin/env bash
# job_02_research_normalize.sh — 规范化调研结果
set -euo pipefail
source "$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)/common.sh"
SCOPE="${1:?scope required}"

DOC_DIR="${DOCS_DIR}/01-research"
ensure_dir "$DOC_DIR"

write_doc "$DOC_DIR/normalized-requirements.md" "# 规范化需求清单

> 生成时间: $(now_ts)
> 执行范围: ${SCOPE}

## 需求规范化说明

本文档将前一阶段的竞品分析和用户调研结果进行规范化整理，形成结构化的需求条目。

## 功能需求清单

### FR-001: 预约管理模块
- **优先级**: P0
- **来源**: 用户调研 + 竞品分析
- **描述**: 支持多医生、多诊室的预约排班与调度
- **子功能**:
  - FR-001.1: 预约创建/修改/取消
  - FR-001.2: 预约日历视图
  - FR-001.3: 预约冲突检测
  - FR-001.4: 预约提醒通知

### FR-002: 患者档案模块
- **优先级**: P0
- **来源**: 用户调研 + 竞品分析
- **描述**: 患者基本信息、就诊历史、病历的统一管理
- **子功能**:
  - FR-002.1: 患者信息 CRUD
  - FR-002.2: 就诊历史时间线
  - FR-002.3: 患者标签与分组
  - FR-002.4: 患者搜索与筛选

### FR-003: 收费结算模块
- **优先级**: P0
- **来源**: 用户调研
- **描述**: 诊疗费用计算、账单生成、收款记录
- **子功能**:
  - FR-003.1: 费用项目管理
  - FR-003.2: 账单生成与打印
  - FR-003.3: 收款与退款
  - FR-003.4: 医保对接(预留)

### FR-004: 电子病历模块
- **优先级**: P1
- **来源**: 医生调研
- **描述**: 结构化口腔病历录入与管理
- **子功能**:
  - FR-004.1: 病历模板管理
  - FR-004.2: 牙位图标注
  - FR-004.3: 治疗方案录入
  - FR-004.4: 病历打印与导出

### FR-005: 库存管理模块
- **优先级**: P1
- **来源**: 管理者调研
- **描述**: 耗材与器材的入库、出库、盘点
- **子功能**:
  - FR-005.1: 库存台账
  - FR-005.2: 入库/出库记录
  - FR-005.3: 库存预警
  - FR-005.4: 供应商管理

### FR-006: 报表与分析模块
- **优先级**: P1
- **来源**: 管理者调研
- **描述**: 经营数据统计与可视化
- **子功能**:
  - FR-006.1: 营收报表
  - FR-006.2: 患者统计
  - FR-006.3: 医生工作量统计
  - FR-006.4: 自定义报表

## 非功能需求

| ID | 类别 | 描述 | 优先级 |
|----|------|------|--------|
| NFR-001 | 性能 | 页面加载 < 2s, API 响应 < 500ms | P0 |
| NFR-002 | 安全 | 数据加密存储、RBAC 权限控制 | P0 |
| NFR-003 | 可用性 | 99.9% SLA | P1 |
| NFR-004 | 可扩展 | 支持多租户、插件化 | P1 |
| NFR-005 | 合规 | 符合医疗数据相关法规 | P0 |
"

echo "$DOC_DIR/normalized-requirements.md"
