#!/usr/bin/env bash
# job_02_research_normalize.sh — 规范化调研结果（东南亚市场 + 仓储/商城）
set -euo pipefail
source "$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)/common.sh"
SCOPE="${1:?scope required}"

DOC_DIR="${DOCS_DIR}/01-research"
ensure_dir "$DOC_DIR"

write_doc "$DOC_DIR/normalized-requirements.md" "# 规范化需求清单

> 生成时间: $(now_ts)
> 执行范围: ${SCOPE}
> 目标市场: 东南亚 (Southeast Asia)

## 需求规范化说明

本文档将前一阶段的东南亚市场竞品分析和用户调研结果进行规范化整理，形成结构化的需求条目。

## 功能需求清单

### FR-001: 预约管理模块
- **优先级**: P0
- **来源**: 用户调研 + 竞品分析
- **描述**: 支持多医生、多诊室的预约排班与调度，支持牙科旅游患者在线预约
- **子功能**:
  - FR-001.1: 预约创建/修改/取消
  - FR-001.2: 预约日历视图
  - FR-001.3: 预约冲突检测
  - FR-001.4: 预约提醒通知（WhatsApp/LINE/SMS）
  - FR-001.5: 在线预约（面向牙科旅游患者）

### FR-002: 患者档案模块
- **优先级**: P0
- **来源**: 用户调研 + 竞品分析
- **描述**: 患者基本信息、就诊历史、病历的统一管理，支持多语言档案
- **子功能**:
  - FR-002.1: 患者信息 CRUD
  - FR-002.2: 就诊历史时间线
  - FR-002.3: 患者标签与分组
  - FR-002.4: 患者搜索与筛选
  - FR-002.5: 多语言患者信息支持

### FR-003: 收费结算模块
- **优先级**: P0
- **来源**: 用户调研
- **描述**: 诊疗费用计算、账单生成、收款记录，支持多币种与东南亚本地支付
- **子功能**:
  - FR-003.1: 费用项目管理
  - FR-003.2: 账单生成与打印
  - FR-003.3: 收款与退款
  - FR-003.4: 多币种支持（THB/VND/IDR/MYR/SGD/PHP/USD）
  - FR-003.5: 本地支付集成（GrabPay/GCash/OVO/银行转账）
  - FR-003.6: 私人保险对接（预留）

### FR-004: 电子病历模块
- **优先级**: P1
- **来源**: 医生调研
- **描述**: 结构化口腔病历录入与管理
- **子功能**:
  - FR-004.1: 病历模板管理
  - FR-004.2: 牙位图标注
  - FR-004.3: 治疗方案录入
  - FR-004.4: 病历打印与导出
  - FR-004.5: 双语/多语言病历支持

### FR-005: 临床库存管理模块
- **优先级**: P1
- **来源**: 管理者调研
- **描述**: 诊所内部耗材与器材的日常领用管理
- **子功能**:
  - FR-005.1: 科室领用记录
  - FR-005.2: 耗材消耗统计
  - FR-005.3: 低库存预警
  - FR-005.4: 与仓储系统联动

### FR-006: 仓储管理模块 (WMS)
- **优先级**: P0
- **来源**: 管理者调研 + 竞品差异化
- **描述**: 面向口腔耗材/器材/口腔护理产品的仓库管理系统，支持多仓库、多供应商、进销存全流程，为商城系统提供库存底层支撑
- **子功能**:
  - FR-006.1: 仓库管理（多仓库、库位管理）
  - FR-006.2: 入库管理（采购入库/退货入库/调拨入库）
  - FR-006.3: 出库管理（销售出库/领用出库/调拨出库）
  - FR-006.4: 库存盘点（定期/动态盘点）
  - FR-006.5: 供应商管理（供应商档案/评级/合同）
  - FR-006.6: 采购管理（采购申请/审批/订单/到货）
  - FR-006.7: 库存预警与自动补货建议
  - FR-006.8: 批次/效期管理（耗材有效期追踪）
  - FR-006.9: 进口耗材管理（多币种采购/报关信息）
  - FR-006.10: 库存报表与分析

### FR-007: 商城系统模块
- **优先级**: P1
- **来源**: 竞品差异化 + 用户调研
- **描述**: 面向 B2C（患者购买口腔护理产品）和 B2B（诊所间耗材采购）的商城系统，与仓储模块深度集成
- **子功能**:
  - FR-007.1: 商品管理（SPU/SKU、分类、品牌）
  - FR-007.2: 商品上下架与定价
  - FR-007.3: B2C 商城前台（患者端口腔护理产品商城）
  - FR-007.4: B2B 采购平台（诊所间耗材交易）
  - FR-007.5: 订单管理（创建/支付/发货/签收/退换）
  - FR-007.6: 物流跟踪集成
  - FR-007.7: 促销与优惠券
  - FR-007.8: 商城库存与仓储系统同步
  - FR-007.9: 东南亚电商平台对接预留（Shopee/Lazada）

### FR-008: 报表与分析模块
- **优先级**: P1
- **来源**: 管理者调研
- **描述**: 经营数据统计与可视化
- **子功能**:
  - FR-008.1: 营收报表
  - FR-008.2: 患者统计
  - FR-008.3: 医生工作量统计
  - FR-008.4: 库存与采购报表
  - FR-008.5: 商城销售报表
  - FR-008.6: 自定义报表

### FR-009: 多语言与本地化模块
- **优先级**: P1
- **来源**: 东南亚市场特殊需求
- **描述**: 系统界面、通知、文档的多语言支持
- **子功能**:
  - FR-009.1: 界面多语言切换（英语/泰语/越南语/印尼语/马来语等）
  - FR-009.2: 多币种配置
  - FR-009.3: 时区支持
  - FR-009.4: 本地化日期/数字格式

## 非功能需求

| ID | 类别 | 描述 | 优先级 |
|----|------|------|--------|
| NFR-001 | 性能 | 页面加载 < 2s, API 响应 < 500ms | P0 |
| NFR-002 | 安全 | 数据加密存储、RBAC 权限控制 | P0 |
| NFR-003 | 可用性 | 99.9% SLA | P1 |
