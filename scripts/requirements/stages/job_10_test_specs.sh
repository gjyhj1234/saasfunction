#!/usr/bin/env bash
# job_10_test_specs.sh — 生成验收测试文档
set -euo pipefail
source "$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)/common.sh"
SCOPE="${1:?scope required}"

DOC_DIR="${DOCS_DIR}/07-test-specs"
ensure_dir "$DOC_DIR"

write_doc "$DOC_DIR/acceptance-tests.md" "# 验收测试文档

> 生成时间: $(now_ts)
> 执行范围: ${SCOPE}

## 测试策略

### 测试层次
1. **单元测试**: 覆盖核心业务逻辑 (≥80%)
2. **集成测试**: API 端到端验证
3. **E2E 测试**: 关键业务流程
4. **性能测试**: 并发与响应时间

## 验收测试用例

### AT-001: 用户登录
| 项目 | 内容 |
|------|------|
| 前提 | 用户账号已创建 |
| 步骤 | 1. 打开登录页 2. 输入账号密码 3. 点击登录 |
| 预期 | 跳转到工作台，显示用户信息 |
| 异常 | 密码错误时显示提示，5次锁定 |

### AT-002: 创建预约
| 项目 | 内容 |
|------|------|
| 前提 | 用户已登录，有可用医生和时段 |
| 步骤 | 1. 进入预约管理 2. 点击新建 3. 选择患者 4. 选择医生 5. 选择时段 6. 确认 |
| 预期 | 预约创建成功，显示在日历中 |
| 异常 | 时间冲突时给出提示 |

### AT-003: 预约改期
| 项目 | 内容 |
|------|------|
| 前提 | 存在一个 scheduled 状态的预约 |
| 步骤 | 1. 打开预约详情 2. 点击改期 3. 选择新时段 4. 确认 |
| 预期 | 预约时间更新，通知患者 |
| 异常 | 新时段冲突时给出提示 |

### AT-004: 预约取消
| 项目 | 内容 |
|------|------|
| 前提 | 存在一个 scheduled/confirmed 的预约 |
| 步骤 | 1. 打开预约详情 2. 点击取消 3. 填写原因 4. 确认 |
| 预期 | 预约状态变为 cancelled |
| 异常 | 已完成的预约不可取消 |

### AT-005: 新建患者
| 项目 | 内容 |
|------|------|
| 前提 | 用户已登录，有患者管理权限 |
| 步骤 | 1. 进入患者管理 2. 点击新建 3. 填写信息 4. 保存 |
| 预期 | 患者创建成功，出现在列表中 |
| 异常 | 手机号重复时给出提示 |

### AT-006: 患者搜索
| 项目 | 内容 |
|------|------|
| 前提 | 系统中有患者数据 |
| 步骤 | 1. 进入患者列表 2. 输入搜索关键词 3. 回车 |
| 预期 | 列表显示匹配结果 |
| 异常 | 无匹配时显示空状态 |

### AT-007: 创建收费单
| 项目 | 内容 |
|------|------|
| 前提 | 存在已完成的诊疗预约 |
| 步骤 | 1. 进入收费页面 2. 选择费用项 3. 输入数量 4. 确认金额 |
| 预期 | 账单创建成功，状态为 pending |
| 异常 | 金额为 0 时不允许提交 |

### AT-008: 收款
| 项目 | 内容 |
|------|------|
| 前提 | 存在 pending 的收费单 |
| 步骤 | 1. 打开收费单 2. 选择支付方式 3. 确认收款 |
| 预期 | 状态变为 paid，生成收费记录 |
| 异常 | 收款失败时回滚 |

### AT-009: 权限控制
| 项目 | 内容 |
|------|------|
| 前提 | 不同角色用户已创建 |
| 步骤 | 1. 以不同角色登录 2. 访问各菜单 |
| 预期 | 各角色只能看到授权菜单 |
| 异常 | 直接访问无权限 URL 返回 403 |

### AT-010: 数据隔离
| 项目 | 内容 |
|------|------|
| 前提 | 存在多个租户 |
| 步骤 | 1. 租户 A 创建数据 2. 租户 B 查询 |
| 预期 | 租户 B 无法看到租户 A 的数据 |
| 异常 | API 层面也不返回跨租户数据 |

## 性能测试指标

| 场景 | 并发用户 | 预期响应时间 | 通过条件 |
|------|----------|-------------|----------|
| 登录 | 100 | < 1s | P99 < 2s |
| 列表查询 | 50 | < 500ms | P99 < 1s |
| 创建预约 | 30 | < 800ms | P99 < 1.5s |
| 收费操作 | 20 | < 1s | P99 < 2s |
"

echo "$DOC_DIR/acceptance-tests.md"
