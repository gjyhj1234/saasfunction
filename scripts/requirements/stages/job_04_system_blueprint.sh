## 服务划分

| 服务 | 职责 | 技术栈 |
|------|------|--------|
| api-gateway | 请求路由、认证、限流 | NestJS |
| appointment-service | 预约管理 | NestJS + TypeORM |
| patient-service | 患者档案 | NestJS + TypeORM |
| billing-service | 收费结算 | NestJS + TypeORM |
| medical-record-service | 电子病历 | NestJS + TypeORM |
| inventory-service | 库存管理 | NestJS + TypeORM |
| report-service | 报表分析 | NestJS + TypeORM |
| auth-service | 用户认证与权限 | NestJS + Passport |
| notification-service | 消息通知 | NestJS |

## 部署架构

- **容器编排**: Kubernetes
- **CI/CD**: GitHub Actions
- **监控**: Prometheus + Grafana
- **日志**: ELK Stack
- **CDN**: CloudFlare / Aliyun CDN

## 通信模式

- 同步: REST API (服务间)
- 异步: 消息队列 (事件驱动)
- 实时: WebSocket (通知推送)
