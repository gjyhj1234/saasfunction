#!/usr/bin/env bash
# job_08_page_design.sh — 生成页面详细设计
set -euo pipefail
source "$(cd "$(dirname "${BASH_SOURCE[0]}")/.." && pwd)/common.sh"
SCOPE="${1:?scope required}"

DOC_DIR="${DOCS_DIR}/06-pages"
ensure_dir "$DOC_DIR"

write_doc "$DOC_DIR/page-specifications.md" "# 页面详细设计

> 生成时间: $(now_ts)
> 执行范围: ${SCOPE}

## 页面清单

### P-001: 登录页
- **路由**: \`/login\`
- **布局**: 居中卡片
- **组件**:
  - Logo + 产品名称
  - 账号输入框
  - 密码输入框
  - 记住我 checkbox
  - 登录按钮
  - 忘记密码链接
- **交互**:
  - 表单验证（必填、格式）
  - 登录中 loading 状态
  - 错误提示（toast）
  - 成功后跳转工作台

### P-002: 工作台
- **路由**: \`/dashboard\`
- **布局**: 侧边栏 + 顶栏 + 内容区
- **组件**:
  - 今日预约统计卡片
  - 营收统计卡片
  - 待办事项列表
  - 预约时间线
  - 快捷操作按钮组
- **数据源**: Dashboard API

### P-003: 预约日历
- **路由**: \`/appointments/calendar\`
- **布局**: 主内容区
- **组件**:
  - 日历组件 (日/周/月切换)
  - 预约卡片 (显示患者名、医生、时段)
  - 新建预约快捷按钮
  - 筛选栏 (医生、诊室)
- **交互**:
  - 拖拽调整预约时间
  - 点击空白时段创建预约
  - 点击预约查看详情

### P-004: 预约列表
- **路由**: \`/appointments/list\`
- **布局**: 表格页
- **组件**:
  - 搜索栏
  - 筛选器 (状态、医生、日期范围)
  - 数据表格
  - 分页控件
  - 批量操作按钮
- **表格列**: 预约时间、患者、医生、诊疗类型、状态、操作

### P-005: 患者列表
- **路由**: \`/patients/list\`
- **布局**: 表格页
- **组件**:
  - 搜索栏 (姓名、手机号)
  - 标签筛选
  - 数据表格
  - 分页控件
- **表格列**: 姓名、性别、年龄、手机号、最近就诊、标签、操作

### P-006: 患者详情
- **路由**: \`/patients/:id\`
- **布局**: 详情页 (标签页)
- **标签页**:
  - 基本信息
  - 就诊记录
  - 费用记录
  - 病历记录
  - 影像资料

### P-007: 收费页面
- **路由**: \`/billing/charge/:visit_id\`
- **布局**: 两栏 (左: 费用项, 右: 汇总)
- **组件**:
  - 费用项目选择器
  - 已选项目列表 (可调数量)
  - 折扣输入
  - 费用汇总
  - 支付方式选择
  - 确认收费按钮

### P-008: 系统设置
- **路由**: \`/settings\`
- **布局**: 左侧菜单 + 右侧内容
- **子页面**:
  - 机构信息设置
  - 员工管理
  - 角色权限
  - 费用项目
  - 系统参数

## 通用 UI 规范

### 颜色系统
- 主色: #2563EB (蓝色)
- 成功: #16A34A
- 警告: #D97706
- 错误: #DC2626
- 中性: #6B7280

### 字体
- 标题: 16-24px, font-weight: 600
- 正文: 14px, font-weight: 400
- 辅助: 12px, font-weight: 400

### 间距
- 页面内边距: 24px
- 卡片间距: 16px
- 组件内间距: 12px
"

echo "$DOC_DIR/page-specifications.md"
